#include <ctime>
#include <iostream>
#include <time.h>
#include <stdio.h>
#include "nk4.h"
#include "nk5.h"

int main()
{
    NK4 * nk4 = new NK4(0,10,1000);
    double result = 0.0;
    unsigned start_time = clock();
    nk4->run(result);
    unsigned end_time = clock();
    
    printf("Result : %0.8f \n", result);
    printf("Time : %0.8f \n", double(end_time - start_time)/CLOCKS_PER_SEC);

    NK5 * nk5 = new NK5(0,10,1000);
    result = 0.0;
    start_time = clock();
    nk5->run(result);
    end_time = clock();
    
    printf("Result : %0.8f \n", result);
    printf("Time : %0.8f \n", double (end_time - start_time)/CLOCKS_PER_SEC);
}
